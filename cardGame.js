
var iconClasses = [
  "fas fa-frog", "fas fa-fish", "fas fa-hippo", "fas fa-hamburger", 
  "fas fa-pizza-slice", "fas fa-hotdog", "fas fa-drumstick-bite", "fas fa-carrot", 
  "fas fa-carrot", "fas fa-drumstick-bite", "fas fa-hotdog", "fas fa-pizza-slice", 
  "fas fa-hippo", "fas fa-hamburger", "fas fa-frog", "fas fa-fish"
]

// set the classes to the target html components
// this is the container that will hold all the cards
var cardToAdd = document.createDocumentFragment();
for(var i = 0; i < iconClasses.length; i++){
   // <div class="box"><i class="fas fa-frog"></i></div>
   var newITag = document.createElement('i');
   var newDiv = document.createElement('div');
   newDiv.className = 'box';
   newDiv.id = "box" + i;
   
   newITag.class = iconClasses[i];
   newDiv.appendChild(newITag);
   cardToAdd.append(newDiv);
}
document.getElementById('cards').appendChild(cardToAdd);
var elapsed = 5;

var x = setInterval(function() {
  document.getElementById("countDownTimer").innerHTML = "00:00:0" + elapsed;
  elapsed = elapsed - 1;

  if (elapsed < 0) {
    clearInterval(x);
    elapsed = 0;
    document.getElementById("countDownTimer").innerHTML = "EXPIRED";

    document.getElementById('cards').innerHTML = '';
    for(var i = 0; i < iconClasses.length; i++){
       // <div class="box"><i class="fas fa-frog"></i></div>
       var newDiv = document.createElement('div');
       newDiv.className = 'box';
       newDiv.onclick = numberTrack;
       newDiv.id = i;
       
       var newITag = document.createElement('i');
       newDiv.appendChild(newITag);
       cardToAdd.appendChild(newDiv);
    }
    document.getElementById('cards').appendChild(cardToAdd);
  }
}, 1000);

var clickedItems = []
var totalItems = 0;

function getValue(e) {
    totalItems = totalItems + 1;
    clickedItems.push(e.target.id);

    if (clickedItems.length == 2) {
      
      for (var i = 0; i < 2; i++) {

        var firstChoice = parseInt(clickedItems[0]);
        var secondChoice = parseInt(clickedItems[1]);

        if (firstChoice === secondChoice) {
          console.log("Please select two different boxes");
        } else if (iconClasses[firstChoice] === iconClasses[secondChoice]) {
          document.getElementById(firstChoice).innerHTML = "<i class='fas fa-check'></i>";
          document.getElementById(secondChoice).innerHTML = "<i class='fas fa-check'></i>";
        } else {
          document.getElementById(firstChoice).innerHTML = "<i class='fas fa-times-circle'></i>";
          document.getElementById(secondChoice).innerHTML = "<i class='fas fa-times-circle'></i>";
        }

      }
      // reset it so, the user can make new picks
      clickedItems = [];
      if (totalItems == 16) {
        document.getElementById("countDownTimer").innerHTML = "GAME OVER! Please Reload the page.";
      }
    }
}

function numberTrack() {
    var gridItems = document.getElementsByClassName('box');
    for (var i = 0; i < gridItems.length; i++) {
        gridItems[i].onclick = getValue;
    }
}

















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































